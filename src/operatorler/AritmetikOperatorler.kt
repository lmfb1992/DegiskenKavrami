package operatorler

fun main() {

    var sayi1: Int = 15
    var sayi2 = 3

    println("Sayilarin toplami "+(sayi1+sayi2))
    println("Sayilarin farki ${sayi1-sayi2}")


    println("Sayilarin Carpimi "+(sayi1*sayi2))
    println("Sayilarin bolumu "+(sayi1/sayi2))

    // % mod almaya yarar. kısacası iki sayının bölümünden kalanı verir.

    println("Sayi1 % sayi2  "+(sayi1%sayi2))







}

